# 07. Move Elements Part 2 
# 方法 1
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
L.insert(len(L), L.pop(0))
print(L)  # [2, 3, 4, 5, 6, 7, 8, 9, 1]

# 方法 2
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
result = L[1:] + L[:1]
print(result)  # [2, 3, 4, 5, 6, 7, 8, 9, 1]

# 不建議的方法
## 若列表中有兩個 1，只會移除第一個，不一定是最後一個
## 假設資料第一個一定是 1，通用性較差
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
L.remove(1)
L.append(1)
print(L)  # [2, 3, 4, 5, 6, 7, 8, 9, 1]
