# 06. Move Elements Part 1 
# 方法 1
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
last = L.pop()  # 取出最後一個元素
L.insert(0, last)  # 插到最前面
print(L)  # [9, 1, 2, 3, 4, 5, 6, 7, 8]

# 方法 2：slicing
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
result = L[-1:] + L[:-1]
print(result)  # [9, 1, 2, 3, 4, 5, 6, 7, 8]

# 不建議的方法
## 若列表中有兩個 9，只會移除第一個，不一定是最後一個
## 假設資料最後一個一定是 9，通用性較差
L = [1, 2, 3, 4, 5, 6, 7, 8, 9]
L.remove(9) 
L.insert(0, 9)
print(L)  # [9, 1, 2, 3, 4, 5, 6, 7, 8]
